// @lc code=start
/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number}
 */
var findBottomLeftValue = function(root) {
  let maxDegree = 0
  let ret = root.val

  const traverse = (node, prevDegree) => {
    if (!node) return

    const currentDegree = prevDegree + 1
    if (currentDegree > maxDegree) {
      maxDegree = currentDegree
      ret = node.val
    }

    traverse(node.left, currentDegree + 1)
    traverse(node.right, currentDegree + 1)
  }

  traverse(root, 0)

  return ret
};
