import { TreeNode } from './commons/Tree'

export
// @lc code=start
function maxDepth(root: TreeNode | null): number {
  if (!root) return 0
  const left = maxDepth(root.left), right = maxDepth(root.right)
  return Math.max(left, right) + 1
}
